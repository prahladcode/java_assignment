public class CreateArrays {
    public static void main(String[] args) {
        
        int[] integers = new int[10];

        
        float[] floats = new float[5];

       
        double[] doubles = new double[8];

       
        short[] shorts = new short[4];

    
        byte[] bytes = new byte[5];

        
        System.out.println("Number of integers: " + integers.length);
        System.out.println("Number of floats: " + floats.length);
        System.out.println("Number of doubles: " + doubles.length);
        System.out.println("Number of shorts: " + shorts.length);
        System.out.println("Number of bytes: " + bytes.length);
    }
}
